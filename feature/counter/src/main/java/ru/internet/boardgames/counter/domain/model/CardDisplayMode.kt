package ru.internet.boardgames.counter.domain.model

/**
 * Режим отображения карточек на главном экране (§5 ТЗ).
 *
 * [COMPACT] — вертикальные карточки в две колонки (§5.1):
 *   тёмный верх с крупным значением (~48sp) + цветной низ с кнопками −/+.
 *
 * [WIDE] — горизонтальные «таблетки» на всю ширину (§5.2):
 *   фон = цвет счётчика, [−] слева, значение (~40sp) по центру, [+] справа.
 */
enum class CardDisplayMode {
    COMPACT,
    WIDE
}
